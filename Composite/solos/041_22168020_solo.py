def p(j,A=0):
 for c in j:
  for E,k in enumerate(c):
   if k:A=(not A)*k
   else:c[E]=A
 return j


# --- Code Golf Solution (Compressed) ---
def q(m, c=0):
    return [[v | (c := (c ^ v)) for v in r] for r in m]


